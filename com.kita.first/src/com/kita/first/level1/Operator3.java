package com.kita.first.level1;

public class Operator3 {
	public static void main(String[] args) {
		int n1 = 2;
		int n2 = 3;
		System.out.println(n1 + n2);
		System.out.println(n1 - n2);
		System.out.println(n1 * n2);
		System.out.println(n1 / n2);
		System.out.println(n1 % n2);
		double result1 = (double)n1 / n2;
		System.out.println(result1);
	}

}
